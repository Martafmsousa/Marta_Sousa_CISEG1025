# Exercicio 1 - Crie um algoritmo que mostre os 30 primeiros números ímpares e pares

# Pares
print("Primeiros 30 números pares:")
contador = 0
num = 2

while contador < 30:
    print(num)
    num = num + 2
    contador = contador + 1

# Ímpares
print("\nPrimeiros 30 números ímpares:")
contador = 0
num = 1

while contador < 30:
    print(num)
    num = num + 2
    contador = contador + 1
